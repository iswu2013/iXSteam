package bean;

public class capital {

    private String custorName;
    private String trade_sn;
    private String import_type;
    private String acctNo;
    private String money;
    private String returnCode;
    
    public String getCustorName() {
        return custorName;
    }
    public void setCustorName(String custorName) {
        this.custorName = custorName;
    }
    public String getTrade_sn() {
        return trade_sn;
    }
    public void setTrade_sn(String trade_sn) {
        this.trade_sn = trade_sn;
    }
    public String getImport_type() {
        return import_type;
    }
    public void setImport_type(String import_type) {
        this.import_type = import_type;
    }
    public String getAcctNo() {
        return acctNo;
    }
    public void setAcctNo(String acctNo) {
        this.acctNo = acctNo;
    }
    public String getMoney() {
        return money;
    }
    public void setMoney(String money) {
        this.money = money;
    }
    public String getReturnCode() {
        return returnCode;
    }
    public void setReturnCode(String returnCode) {
        this.returnCode = returnCode;
    }
    
}
